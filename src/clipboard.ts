// Copy text to the clipboard. Prefers the async Clipboard API, which needs a
// secure context (https or localhost). TabTerm is often served over plain http
// on the LAN, where it's unavailable — so fall back to a hidden textarea +
// document.execCommand("copy"). Returns true on success.
export async function copyText(text: string): Promise<boolean> {
  try {
    if (navigator.clipboard && window.isSecureContext) {
      await navigator.clipboard.writeText(text);
      return true;
    }
  } catch {
    // fall through to legacy path
  }
  try {
    const ta = document.createElement("textarea");
    ta.value = text;
    ta.style.position = "fixed";
    ta.style.opacity = "0";
    document.body.appendChild(ta);
    ta.focus();
    ta.select();
    const ok = document.execCommand("copy");
    document.body.removeChild(ta);
    return ok;
  } catch {
    return false;
  }
}
